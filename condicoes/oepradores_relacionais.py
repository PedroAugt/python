"""
Operadores Relacionais
== > >= < <= !=
"""
num_1 = 2
num_2 = 2

expressao_igualdade = (num_1 == num_2)
expressao_maior = (num_1 > num_2)
expressao_maior_igual = (num_1 >= num_2)
expressao_menor = (num_1 < num_2)
expressao_menor_igual = (num_1 <= num_2)
expressao_diferente = (num_1 != num_2)

print(expressao_igualdade)
print(expressao_maior)
print(expressao_menor_igual)
print(expressao_menor)
print(expressao_menor_igual)
print(expressao_diferente)


